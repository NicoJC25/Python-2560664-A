#Al igual que con numeros, se pueden utilizar operaciones para concatenar o replicar cadenas. Ejemplo:

'''str1 = 'a'
str2 = 'b'

print(str1 + str2)
print(str2 + str1)
print(5 * 'a')
print('b' * 4)'''
#Nota: Tambien se pueden usar atajos como "+=" o "*="

#Para saber el punto de codigo de una cadena utilizamos la funcion "ord" de la siguiente forma:

'''char_1 = 'a'
char_2 = ' '  # space

print(ord(char_1))
print(ord(char_2))'''

#Si se desea hacer lo contrario, de identificar un caracter con un punto de codigo, usamos la funcion "chr" de la siguiente forma:

'''print(chr(97))
print(chr(945))'''



