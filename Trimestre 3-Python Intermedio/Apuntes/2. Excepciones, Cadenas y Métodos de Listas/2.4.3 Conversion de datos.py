#Ahora vamos a ver la forma de convertir un numero entero o flotante, en cadena y viceversa.

#La funcion "str"

#Esta funcion, recibe como argumento una variable o elemento y lo convierte en cadena. Ejemplo:

'''itg = 13
flt = 1.3
si = str(itg)
sf = str(flt)

print(si + ' ' + sf)'''


#Las funciones "int" y "float"

#Con la funcion "int", conviertes un elemento en entero y con la funcion "float", un elemento en flotante (decimal). Ejemplo:

'''si = '13'
sf = '1.3'
itg = int(si)
flt = float(sf)

print(itg + flt)'''

