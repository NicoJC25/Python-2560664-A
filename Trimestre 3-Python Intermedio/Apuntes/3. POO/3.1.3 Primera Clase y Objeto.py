#Para crear una primera clase, utilizamos la siguiente sintaxis:

class MiPrimeraClase:
    pass

#Utilizamos la palabra reservada "class", seguida del nombre de la clase y como si fuera una funcion...
#...identamos para poner lo que necesitemos dentro de la clase. En este caso la palabra reservada "pass"...
#...que sirve para indicar que la clase esta vacia.

#Ahora que creamos la clase, podemos usarla como herramienta para un objeto de la siguiente forma:

MiPrimerObjeto=MiPrimeraClase()

#Creamos una variable (en este caso objeto) y le damos como valor el nombre de la clase seguida por unos parentesis.

#Esto se conoce como instanciacion ya que estamos haciendo una instancia de la clase