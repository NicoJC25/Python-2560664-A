#Durante todo lo anteriormente visto, se uso un tipo de programacion llamada "Programacion procedimental"
#La cual aunque se ha usado durante muchas decadas, ha sido muy util durante todo el desarrollo de la programacion

#Desde hace poco, nacio la programacion orientada a objetos "POO", la cual aunque es bastante reciente en comparacion con la otra...
#...es muy util para proyectos con bastante complejidad.

#De esta forma, este tipo de programacion divide el proyecto en varias partes e independientes para un mejor desarrollo



#En el enfoque procedimental, hay dos mundos diferentes: El mundo de los datos y el mundo del codigo.
#En el de los datos, estan todas las variables de todos los tipo y en el del codigo, estos mismos agrupados en modulos y funciones.

#En cierto modo, las funciones pueden usar datos y abusar de ellos pero no al reves, aunque no es del todo cierto

#Existen los metodos, que son funciones invocadas desde dentro de los datos, siendo este el recurso principal en POO


#En el enfoque orientado a objetos, la metodologia cambia ya que se indica que tanto los datos como los codigos estan...
#...encapsulados en el mismo mundo, divididos en clases.

#Cada clase tiene uno o muchos objetos, los cuales estan ahi para resolver algun problema.

#Cada objeto tiene un conjunto de rasgos: Atributos.
#Ademas de poder hacer un conjunto de actividades: Metodos.


#Los objetos van interactuando entre si ya sea cambiando datos o activando sus metodos
#Si se construye una clase de forma correcta, se pueden proteger datos sensibles y ocultarlos para evitar su modificacion

