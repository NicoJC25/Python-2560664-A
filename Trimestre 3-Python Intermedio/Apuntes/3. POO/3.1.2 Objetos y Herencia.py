#La clase es un conjunto de objetos. Un objeto es algo que pertenece a una clase.

#Es por asi decirlo la encarnacion o forma de los requisitos, rasgos y cualidades de la clase.

#Si un objeto pertenece a una subclase, pertenece a todas sus superclases, ya que son como una jerarquia que adopta...
#... lo que sus superclases dejan.

#Por ejemplo: Un automovil pertenece a los vehiculos terrestres que a su vez pertenece a su superclase de vehiculos


#Un concepto fundamental de la POO es la "Herencia".
#Cualquier objeto de una subclase, hereda todos los rasgos de sus superclases.

#Por ejemplo, una clase "C" que pertenece a la clase "B" y tambien a la clase "A", hereda todas las caracteristicas...
#...tanto de la clase B como de la clase A

#Por ejemplo: Un automovil tiene las caracteristicas de su clase (Vehiculo terrestre) pero a su vez...
#...hereda las caracteristicas de su superclase Vehiculos, ya que aunque sea terrestre, su concepto de ser...
#...un objeto artificialmente creado para la movilidad, aun aplica para el mismo. Sin este concepto, no existiria el automovil.


