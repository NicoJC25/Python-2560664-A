#La clase se puede definir como una categoria siendo el resultado de similitudes definidas con presicion

#Para poder mejorar una clase, tenemos que realizar lo que son clases especializadas o "subclases". Por ejemplo:

#Tenemos una clase llamada "Vehiculos", pero para hacer que esa clase se diferencia de otras cosas, hay que especificar...
#...de forma mas detallada esos vehiculos, por ejemplo tomando el concepto de que son un elemento artificialmente creado para la movilidad.
#De ahi ya partimos a varios tipos de vehiculos: Terrestres, Acuaticos, Aereos, Espaciales.

#La forma de organizarse de estas clases es como si fueran las raices de un arbol: La clase principal mas arriba...
#...seguida por sus subclases mas abajo.

#Y aun asi, se puede especificar mas las clases como por ejemplo tipos de vehiculos terrestres: Con ruedas, orugas o aerodeslizadores.

